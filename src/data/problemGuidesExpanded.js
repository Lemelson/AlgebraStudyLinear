function attachExactStatements(items) {
  return Object.fromEntries(items.map(({ groupId, number, text }) => {
    const key = `${groupId}:${number}`;
    return [key, { key, groupId, number, exactStatement: text, ...content[key] }];
  }));
}

const officialItems = corpusGroups.problems.flatMap((group) =>
  group.items.map((item) => ({ groupId: group.id, number: item.number, text: item.text })),
);

export const problemGuidesExpanded = attachExactStatements(officialItems);

export function validateProblemGuideCoverage(groups = corpusGroups.problems) {
  const expectedKeys = groups.flatMap((group) => group.items.map((item) => `${group.id}:${item.number}`));
  const actualKeys = Object.keys(content);
  const missing = expectedKeys.filter((key) => !content[key]);
  const extra = actualKeys.filter((key) => !expectedKeys.includes(key));
  const incomplete = expectedKeys.filter((key) => {
    const guide = content[key];
    return !guide || !guide.classification || guide.algorithm?.length < 4 || !guide.keyComputations?.length || !guide.answer || !guide.verification || !guide.commonMistake || !guide.source;
  });
  return {
    expected: expectedKeys.length,
    covered: expectedKeys.length - missing.length,
    missing,
    extra,
    incomplete,
    complete: missing.length === 0 && extra.length === 0 && incomplete.length === 0,
  };
}

export const problemGuideCoverage = validateProblemGuideCoverage();
