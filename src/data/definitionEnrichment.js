function expandedChildExplanation(guide) {
  return [
    guide.simple,
    'Сначала не пытайтесь запомнить всю строгую фразу: найдите объект, о котором говорится, и действие, которое с ним разрешено.',
    `Главный ориентир здесь такой: ${guide.formal}`,
    `Самый первый пример для проверки: ${guide.example}`,
    'Проговорите условие своими словами, затем отдельно укажите, почему пример действительно удовлетворяет каждому требованию.',
    `Особенно важно не сделать типичную подмену: ${guide.pitfall}`,
  ].join(' ');
}

function buildDefinitionEnrichment() {
  const result = {};
  const groupOffsets = {};

  for (const [key, guide] of Object.entries(definitionGuides)) {
    const index = groupOffsets[guide.groupId] ?? 0;
    const latexDefinition = LATEX_BY_GROUP[guide.groupId]?.[index];
    const meta = TOPIC_META[guide.topicId];
    if (!latexDefinition || !meta) {
      throw new Error(`Missing definition enrichment source for ${key}`);
    }

    result[key] = {
      latexDefinition,
      symbolLegend: meta.legend,
      childExplanation: expandedChildExplanation(guide),
      mentalModel: meta.model,
      whyItMatters: meta.why,
      secondExample: `${meta.second} Для текущего определения сравните это с исходным примером: ${guide.example}`,
      counterexample: `Граница понятия хорошо видна на ошибке: ${guide.pitfall} Это не мелкая оговорка, а пример ситуации, где хотя бы одно обязательное условие определения нарушено.`,
      checkQuestion: `Объясните без терминов, что проверяет это определение, и покажите выполнение всех условий на примере «${guide.example}».`,
      checkAnswer: `Нужно назвать объект и обязательные условия из точной формулировки, затем проверить их по одному. Короткий смысл: ${guide.simple}`,
      prerequisites: meta.prerequisites,
      source: guide.source,
    };
    groupOffsets[guide.groupId] = index + 1;
  }

  return result;
}

export const definitionEnrichment = buildDefinitionEnrichment();

export function getDefinitionEnrichment(groupId, number) {
  return definitionEnrichment[`${groupId}:${Number(number)}`] ?? null;
}

const expectedKeys = Object.keys(definitionGuides);
const actualKeys = Object.keys(definitionEnrichment);
const missingKeys = expectedKeys.filter((key) => !definitionEnrichment[key]);

export const definitionEnrichmentCoverage = {
  expected: expectedKeys.length,
  actual: actualKeys.length,
  missingKeys,
  complete: missingKeys.length === 0 && actualKeys.length === expectedKeys.length,
};

if (!definitionEnrichmentCoverage.complete) {
  throw new Error(`Incomplete definition enrichment: ${missingKeys.join(', ')}`);
}
